# 加 +
a = 2 + 3
print(a)
# 字符串相加 仅仅是组合起来
a = '2' + '3'
print(a)
# 减 -
a = 2 - 3
print(a)
# 乘 *
b = 2 * 8
print(b)
# 乘方 **
c = 3 ** 3
# （即3x3x3x3 ）
print(c)
# 除 /
d = 13 / 3
print(d)
# 整除 //
e = 13 // 3
# x 除以 y 并对结果向下取整至最接近的整数
print(e)
# 取模 %
f = 13 % 3
# 返回除法运算后的余数。
print(f)
f = 10 % 5
print(f)

g = 11 >> 1
print(g)

j = 22 & 2
print(j)

i = 5 < 4
print(i)  # false
i = "k" < "l"
print(i)  # true
# 按位取反
# -(x+1)
i = ~6
print(i)

